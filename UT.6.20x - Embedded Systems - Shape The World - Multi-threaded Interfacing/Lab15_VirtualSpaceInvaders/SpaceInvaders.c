// update all the sprites' position values
void Move (void) {
  // move player ship
  unsigned long ADC_Position = (ADC0_In() * (80 - PLAYERW + 4)) / 4095;
  if (((PlayerShip.x - ADC_Position) < 4) || ((ADC_Position - PlayerShip.x) < 4)) {
    PlayerShip.x = ADC_Position;
  }
  else if ((PlayerShip.x - ADC_Position) < 79) {
    PlayerShip.x -= 4;
  }
  else if ((ADC_Position - PlayerShip.x) < 79) {
    PlayerShip.x += 4;
  }

  // move enemy ships
  for (int i = 0; i < (CurrentLevel * 4); ++i) {      
    if (EnemyDirection == MOVE_RIGHT) {
      Enemy[i].x += 1;
    }
    else if (EnemyDirection == MOVE_LEFT) {
      Enemy[i].x -= 1;
    }

    if (MoveEnemyDown) {
      Enemy[i].y += 1;
    }
  }

  // move player fire,
  if (PlayerFire.life > 0) {
    PlayerFire.y -= 2;

    // and destroy it if it reaches the top edge
    // comparing with positive value as y is unsigned
    if (PlayerFire.y > 79) {
      PlayerFire.life = 0;
    }
  }

  // move special fire left,
  if (PlayerFireSpecial[0].life > 0) {
    PlayerFireSpecial[0].y -= 2;
    PlayerFireSpecial[0].x -= 2;
  }
  // and destroy it if it reaches the top or left edge
  // comparing with positive value as x and y are unsigned
  if ((PlayerFireSpecial[0].x > 79) || (PlayerFireSpecial[0].y > 79)) {
    PlayerFireSpecial[0].life = 0;
  }

  // move special fire right,
  if (PlayerFireSpecial[1].life > 0) {
    PlayerFireSpecial[1].y -= 2;
    PlayerFireSpecial[1].x += 2;
  }
  // and destroy it if it reaches the top or right edge
  // comparing with positive value as y is unsigned
  if ((PlayerFireSpecial[1].x > 76) || (PlayerFireSpecial[1].y > 79)) {
    PlayerFireSpecial[1].life = 0;
  }

  // move enemy fire
  for (int i = 0; i < 3; ++i) {
    if (EnemyFire[i].life != 0) {
      EnemyFire[i].y += 4;
    }

    // and destroy it, if it reaches the edge
    if (EnemyFire[i].y > 79) {
      EnemyFire[i].life = 0;
    }
  }
}

// draw all the sprites into the display buffer
void Draw (void) {
  Nokia5110_ClearBuffer();

  Nokia5110_PrintBMP(PlayerShip.x, PlayerShip.y, PlayerShip.image[CurrentImage], 0);

  // draw bunker, if alive
  if (Bunker.life > 0) {
    Nokia5110_PrintBMP(Bunker.x, Bunker.y, Bunker.image[CurrentImage], 0);
  }

  // draw alive enemies
  for (int i = 0; i < (CurrentLevel * 4); ++i) {
    if (Enemy[i].life > 0) {
      Nokia5110_PrintBMP(Enemy[i].x, Enemy[i].y, Enemy[i].image[CurrentImage], 0);
    }
  }

  // draw player fire, if alive
  if (PlayerFire.life > 0) {
    Nokia5110_PrintBMP(PlayerFire.x, PlayerFire.y, PlayerFire.image[CurrentImage], 0);
  }
  
  // draw player special fire, if alive
  for (int i = 0; i < 2; ++i) {
    if (PlayerFireSpecial[i].life > 0) {
      Nokia5110_PrintBMP(PlayerFireSpecial[i].x, PlayerFireSpecial[i].y, PlayerFireSpecial[i].image[CurrentImage], 0);
    }
  }

  // draw alive enemies
  for (int i = 0; i < 3; ++i) {
    if (EnemyFire[i].life > 0) {
      Nokia5110_PrintBMP(EnemyFire[i].x, EnemyFire[i].y, EnemyFire[i].image[CurrentImage], 0);
    }
  }

  Nokia5110_DisplayBuffer();  // draw buffer
  CurrentImage ^= 1;          // change image for next frame
}

// fire player's regular missile
void FirePlayerMissile (void) {
  // fire the missile only if the previous one is destroyed
  if (PlayerFire.life == 0) {
    PlayerFire.x = PlayerShip.x + 7;
    PlayerFire.y = 39;
    PlayerFire.life = 1;
    Sound_Shoot();
  }
}

// fire player's special missiles
void FirePlayerMissileSpecial (void) {
  // return if the fire is not ready yet
  if (PlayerFireSpecialReady != 0) {
    return;
  }

  // fire the missile only if the both the previous ones are destroyed
  if ((PlayerFireSpecial[0].life == 0) && (PlayerFireSpecial[1].life == 0)) {
    for (int i = 0; i < 2; ++i) {
      PlayerFireSpecial[i].x = PlayerShip.x + 7;
      PlayerFireSpecial[i].y = 39;
      PlayerFireSpecial[i].life = 1;
    }
    PlayerFireSpecialReady = SPECIAL_FIRE_TIMEOUT;
    Sound_Shoot();
  }
}

// fire enemy laser from the next available enemy ship
void FireEnemyLaser (void) {
  for (int i = 0; i < 3; ++i) {
    // look for a laser to fire
    if (EnemyFire[i].life == 0) {
      for (int j = 0; j < (CurrentLevel * 4); ++j) {
        // look for an enemy to fire
        if ((Enemy[j].life != 0) &&
            (Enemy[j].x >= (PlayerShip.x - 7)) && (Enemy[j].x < (PlayerShip.x + 7))) {
          EnemyFire[i].x = Enemy[j].x + 7;
          EnemyFire[i].y = Enemy[j].y + 10;
          EnemyFire[i].life = 1;
          Sound_Highpitch();

          // found the enemy; break out of loop
          break;
        }
      }

      // found and fired a laser; break out of loop
      break;
    }
  }
}

void DetectCollisions (void) {
  // remove dead sprites
  for (int i = 0; i < 12; ++i) {
    if (PlayerShip.image[CurrentImage ^ 1] == BigExplosion0) {
      PlayerShip.life = 0;
    }
    if ((Enemy[i].life > 0) && (Enemy[i].image[CurrentImage ^ 1] == SmallExplosion0)) {
      Enemy[i].life = 0;
    }
    if (Bunker.image[0] == Bunker3) {
      Bunker.life = 0;
    }
  }

  for (int i = 0; i < (CurrentLevel * 4); ++i) {
    // if an enemy touches a bunker, destroy it
    if ((Bunker.life > 0) &&
        (Enemy[i].x >= Bunker.x) && (Enemy[i].x < (Bunker.x + BUNKERW)) &&
       ((Enemy[i].y + ENEMY10H - 1) >= Bunker.y) &&
       ((Enemy[i].y + ENEMY10H - 1) < (Bunker.y + BUNKERH))) {
      Bunker.life = 0;
    }

    // if an enemy touches the player, the game is over
    if ((Enemy[i].x >= PlayerShip.x) && (Enemy[i].x < (PlayerShip.x + PLAYERW)) &&
       ((Enemy[i].y + ENEMY10H - 1) >= PlayerShip.y) &&
       ((Enemy[i].y + ENEMY10H - 1) < (PlayerShip.y + PLAYERH))) {
      PlayerShip.life = 0;
      PlayerShip.image[CurrentImage] = BigExplosion0;
      PlayerShip.image[CurrentImage ^ 1] = BigExplosion1;
      Sound_Explosion();
    }
  }

  // handle player fire
  if (PlayerFire.life > 0) {
    // player fire collides with the bunker
    if ((Bunker.life > 0) &&
        (PlayerFire.x >= Bunker.x) && (PlayerFire.x <= (Bunker.x + BUNKERW)) &&
       ((PlayerFire.y + MISSILEH) >= Bunker.y) &&
       ((PlayerFire.y + MISSILEH) <= (Bunker.y + BUNKERH))) {
      PlayerFire.life = 0;
    }
    
    // player fire collides with an enemy
    for (int i = 0; i < (4 * CurrentLevel); ++i) {
      if ((Enemy[i].life > 0) &&
          (PlayerFire.x >= Enemy[i].x) && (PlayerFire.x <= (Enemy[i].x + ENEMY10W)) &&
          (PlayerFire.y >= Enemy[i].y) && (PlayerFire.y <= (Enemy[i].y + ENEMY10H))) {
        Enemy[i].image[CurrentImage] = SmallExplosion0;
        Enemy[i].image[CurrentImage ^ 1] = SmallExplosion1;

        if ((i >= 0) && (i <= 3)) {
          CurrentScore += 100;
        }
        else if ((i >= 4) && (i <= 7)) {
          CurrentScore += 200;
        }
        else if ((i >= 8) && (i <= 11)) {
          CurrentScore += 300;
        }

        Sound_Killed();

        PlayerFire.life = 0;
      }
    }
  }

  // handle player special fire
  for (int i = 0; i < 2; ++i) {
    if (PlayerFireSpecial[i].life > 0) {
      // player fire collides with the bunker
      if ((Bunker.life > 0) &&
          (PlayerFireSpecial[i].x >= Bunker.x) &&
          (PlayerFireSpecial[i].x <= (Bunker.x + BUNKERW)) &&
         ((PlayerFireSpecial[i].y + MISSILEH) >= Bunker.y) &&
         ((PlayerFireSpecial[i].y + MISSILEH) <= (Bunker.y + BUNKERH))) {
        PlayerFireSpecial[i].life = 0;
      }
      
      // player fire collides with an enemy
      for (int j = 0; j < (CurrentLevel * 4); ++j) {
        if ((Enemy[j].life > 0) &&
            (PlayerFireSpecial[i].x >= Enemy[j].x) &&
            (PlayerFireSpecial[i].x <= (Enemy[j].x + ENEMY10W)) &&
            (PlayerFireSpecial[i].y >= Enemy[j].y) &&
            (PlayerFireSpecial[i].y <= (Enemy[j].y + ENEMY10H))) {
          Enemy[j].image[CurrentImage] = SmallExplosion0;
          Enemy[j].image[CurrentImage ^ 1] = SmallExplosion1;

          if ((j >= 0) && (j <= 3)) {
            CurrentScore += 100;
          }
          else if ((j >= 4) && (j <= 7)) {
            CurrentScore += 200;
          }
          else if ((j >= 8) && (j <= 11)) {
            CurrentScore += 300;
          }

          Sound_Killed();

          PlayerFireSpecial[i].life = 0;
        }
      }
    }
  }

  // handle enemy fire
  for (int i = 0; i < 2; ++i) {
    if (EnemyFire[i].life > 0) {
      // enemy fire collides with the bunker
      if ((Bunker.life > 0) &&
          (EnemyFire[i].x >= Bunker.x) && (EnemyFire[i].x <= (Bunker.x + BUNKERW)) &&
         ((EnemyFire[i].y + 7) >= Bunker.y) &&
         ((EnemyFire[i].y + 7) <= (Bunker.y + BUNKERH))) {
        if (Bunker.image[0] == Bunker0) {
          Bunker.image[0] = Bunker1;
          Bunker.image[1] = Bunker1;
        }
        else if (Bunker.image[0] == Bunker1) {
          Bunker.image[0] = Bunker2;
          Bunker.image[1] = Bunker2;
        }
        else if (Bunker.image[0] == Bunker2) {
          Bunker.image[0] = Bunker3;
          Bunker.image[1] = Bunker3;
        }
        EnemyFire[i].life = 0;
      }

      // enemy fire collides with player
      if ((EnemyFire[i].x >= PlayerShip.x) && (EnemyFire[i].x <= (PlayerShip.x + PLAYERW)) &&
         ((EnemyFire[i].y + 7) >= PlayerShip.y) &&
         ((EnemyFire[i].y + 7) <= (PlayerShip.y + PLAYERH))) {
        PlayerShip.life -= 1;
        if (PlayerShip.life == 0) {
          PlayerShip.image[CurrentImage] = BigExplosion0;
          PlayerShip.image[CurrentImage ^ 1] = BigExplosion1;
          Sound_Explosion();
        }
        EnemyFire[i].life = 0;
      }
    }
  }
}

// delay count*100 ms
void Delay100ms (unsigned long count) {
  unsigned long volatile time;
  while (count > 0) {
    time = 727240;  // 0.1sec at 80 MHz
    while (time) {
      time--;
    }
    count--;
  }
}

// Initialize Timer2A interrupts to trigger at (1 / period) Hz
void Timer2A_Init (unsigned long period) {
  unsigned long volatile delay;
  SYSCTL_RCGCTIMER_R |= 0x04;  // activate timer2
  delay = SYSCTL_RCGCTIMER_R;
  TIMER2_CTL_R = 0x00000000;   // disable timer2A during setup
  TIMER2_CFG_R = 0x00000000;   // configure for 32-bit mode
  TIMER2_TAMR_R = 0x00000002;  // configure for periodic mode, default down-count settings
  TIMER2_TAILR_R = period-1;   // reload value
  TIMER2_TAPR_R = 0;           // bus clock resolution
  TIMER2_ICR_R = 0x00000001;   // clear timer2A timeout flag
  TIMER2_IMR_R = 0x00000001;   // arm timeout interrupt
  NVIC_PRI5_R = (NVIC_PRI5_R&0x00FFFFFF)|0x80000000;	// priority 4
  NVIC_EN0_R = 1<<23;          // enable IRQ 23 in NVIC
  TIMER2_CTL_R = 0x00000001;   // enable timer2A
}

// timer to play sound
void Timer2A_Handler (void) { 
  TIMER2_ICR_R = 0x00000001;  // acknowledge timer2A timeout
  Play();
}

// Initialize SysTick interrupts to trigger at (1 / period) Hz
void SysTick_Init (unsigned long period) {
  NVIC_ST_CTRL_R = 0;         	  // disable SysTick during setup
  NVIC_ST_RELOAD_R = period - 1;  // configure frequency
  NVIC_ST_CURRENT_R = 0;      	  // any write to current clears it
  NVIC_SYS_PRI3_R = (NVIC_SYS_PRI3_R&0x00FFFFFF)|0x20000000;  // priority 1
  NVIC_ST_CTRL_R = 0x0007; 			  // enable, core clock, and interrupts
  Semaphore = 0;
}

/**
 * Does the following (in the given order):
 * 1. Increases difficulty level after every 80 cycles
 * 2. Changes enemy direction and moves them down every 8 cycles
 * 3. Checks if player's special is ready
 * 4. Sets the LED outputs
 * 5. Handles switch inputs
 * 6. Moves all the sprites to their new position
 * 7. Releases the semaphore to let main() update physical display
 */
void SysTick_Handler (void) {
  // flash LEDs
  if (GameOver) {
    LED_Data ^= 0x03;
    LED_Out(LED_Data);
    return;
  }

  // check if game is over
  if (PlayerShip.life == 0) {
    GameOver = 1;
    GameWon = 0;
    LED_Data = 0x01;
  }
  else {
    unsigned char AnyLife = 0;
    for (int i = 0; i < 12; ++i) {
      AnyLife += Enemy[i].life;
    }
    if (!AnyLife) {
      GameOver = 1;
      GameWon = 1;
      LED_Data = 0x01;
    }
  }

  // increase level after every 88 cycles
  if (TimerCount == 88) {
    CurrentLevel = 2;
    EnemyFireFrequency = 14;

    for (int i = 4; i < 8; ++i) {
      Enemy[i].x = Enemy[i - 4].x;
    }
  }
  if (TimerCount == 176) {
    CurrentLevel = 3;
    EnemyFireFrequency = 12;

    for (int i = 8; i < 12; ++i) {
      Enemy[i].x = Enemy[i - 8].x;
    }
  }

  // reverse direction of enemy motion and move them down
  // once they reach the edges
  if (TimerCount != 0) {
    if ((TimerCount % 8) == 0) {
      EnemyDirection ^= 1;
      MoveEnemyDown = 1;
    }
    else {
      MoveEnemyDown = 0;
    }
  }

  // special fire counter and LED handling
  if (PlayerFireSpecialReady > 0) {
    PlayerFireSpecialReady -= 1;
    LED_Data = 0x00;
  }
  else {
    LED_Data = 0x01;  // using PB4 for special fire ready
  }

  // life LED handling
  if (PlayerShip.life == 1) {
    LED_Data |= 0x02; // using PB5 to indicate last life remaining
  }

  LED_Out(LED_Data);

  // handle switch input
  unsigned char SwitchInput = Switch_In();
  if (SwitchInput >= 2) {
    FirePlayerMissileSpecial();
  }
  else if (SwitchInput == 1) {
    FirePlayerMissile();
  }

  // handle enemy fire
  ++EnemyFireReady;
  EnemyFireReady = EnemyFireReady % EnemyFireFrequency;
  if (!EnemyFireReady) {
    FireEnemyLaser();
  }

  Move();         // move sprites to new position
  DetectCollisions();
  Semaphore = 1;  // trigger to update physical display
  ++TimerCount;
}

int main (void){
  TExaS_Init(NoLCD_NoScope);  // set system clock to 80 MHz
  Switch_Init();			 // initialize switches on PE1-0
  ADC0_Init();				 // initialize ADC on PE2
  LED_Init();					 // initialize LEDs on PE5-4
  Sound_Init();				 // initialize DAC on PB3-0 and sound system
  Random_Init(1);			 // initialize random number generator
  Nokia5110_Init();		 // initialize virtual Nokia 5110 display
  EnableInterrupts();  // virtual Nokia uses UART0 interrupts

  Nokia5110_ClearBuffer();
  Init();                       // initialize game screen
  SysTick_Init(80000000/30);    // 30 Hz
  Timer2A_Init(80000000/11000); // 11 kHz

  while (!GameOver) {
    while (Semaphore == 0);
    Semaphore = 0;
    Draw();
  }

  Nokia5110_Clear();

  if (GameWon) {
    Nokia5110_SetCursor(1, 2);
    Nokia5110_OutString("YOU DID IT!");
    Nokia5110_SetCursor(2, 4);
    Nokia5110_OutUDec(CurrentScore);
    Nokia5110_SetCursor(0, 0);
  }
  else {
    Nokia5110_SetCursor(1, 1);
    Nokia5110_OutString("GAME OVER");
    Nokia5110_SetCursor(1, 2);
    Nokia5110_OutString("Nice try,");
    Nokia5110_SetCursor(1, 3);
    Nokia5110_OutString("Earthling!");
    Nokia5110_SetCursor(2, 4);
    Nokia5110_OutUDec(CurrentScore);
    Nokia5110_SetCursor(0, 0);
  }
  
  while (1);
}
